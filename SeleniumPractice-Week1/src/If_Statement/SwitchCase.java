package If_Statement;

public class SwitchCase {

	public static void main(String[] args) {
		// switch case
		
		int i= 3 ;
		
		switch (i) {
		case 1:
			System.out.println("Value is  1 ");
			break;
		case 2:
			System.out.println("value is 2");
			break;
		case 3:
			System.out.println("Value is 3");
			break;
			default:
			System.out.println ("no value found");
			
		}
		
		
		
		

	}

}
